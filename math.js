// Returns the square of a number
function square(n) 
{
  return n * n;
}

// Returns true if the number is even
function isEven(n) 
{
  return n % 2 === 0;
}

// Returns true if the number is odd
function isOdd(n) 
{
  return n % 2 !== 0;
}

